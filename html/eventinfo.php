							<h2><a name="DateAndTime">When is Oozeball?</a></h2>
							<p>This year's Oozeball event will occur on the weekend of April 20 - 22. Friday April 20 at 10pm will be our Oozeball kick-off party, or "Philanthroparty". We encourage all those participating to come by and celebrate the start of another awesome Oozeball event!
							   Saturday April 21 will be the Housed Day. This is when the brothers of Alpha Nu get to duke it out against one another. Sunday April 22 from 9:00am to 5:00pm will be Open Day when the 3 vs 3 tournament begins!</p>
							<h2><a name="Format">What is the format of the tournament?</a></h2>
							<p>Oozeball is a knockout bracket-style tournament. Teams will consist of three members.</p>
							<h2><a name="Cost">What is the cost to participate?</a></h2>
							<p>The entry cost is $15.00 per player with all proceeds going to the Leukemia and Lymphoma Society. The entry fee is payable with credit card, cash, and buzzcard and the fee includes the 2012 Oozeball t-shirt</p>
							<h2><a name="SignUp">How do I sign up?</a></h2>
							<p>Teams or inviduals interested in participating can register by visiting the Teams page and signing up. This year we will be having teams of three compete.</p>
							<h2><a name="Individual">What if I don't have a team of three?</a></h2>
							<p>If you don't have a team, don't worry! Just head over to the Teams page and register as an individual player and we'll place you on a team.</p>
							<h2><a name="Prizes">What do the winners get?</a></h2>
							<p>Besides the fame that comes with being Oozeball champions, the winners will receive prizes donated by our sponsors. This year's prizes are still to be determined.</p>
							<h2><a name="Food">Will there be food and drinks at the event?</a></h2>
							<p>Crepe Revolution will be serving crepes as well as other food and drink will be avaialable.</p>
							<h2><a name="Contact">Who can I contact with additional questions?</a></h2>
							<p>If you have any other questions please contact Zach Meeks, Oozeball Chair, at <a style="color: white" href="mailto:zmeeks3@gatech.edu">zmeeks3@gatech.edu</a>.</p>
