					<p style="font-size: 22px; margin: 10px 0 10px 20px">About Oozeball</p>
					<div id="innerBox" class="nonTeams">
					<p style="width:430px; margin-right: 10px;">Oozeball consists of a knockout bracket-style mud volleyball tournament held in the front yard of the Phi Kappa Sigma fraternity house at Georgia Tech. It is held every year during the spring, usually during late March or early April. The participants compete for prizes which are predetermined through donations from our corporate sponsors.</p>
					<img src="images/aboutImg1.jpg" alt="The Alpha Nu Chapter Botherhood" title="The Alpha Nu Chapter Brotherhood" style="float: right; margin: 10px 10px 0 0; box-shadow: 5px 5px 10px #000;"/>
					<p style="width: 430px;">All of the money raised from this event goes to the Leukemia and Lymphoma Society which then uses the money to help fund treatments for some of the almost one million Americans living with leukemia, Hodgkin and non-Hodgkin lymphoma, NHL, or myeloma. Also, they are funding the research to help reduce the number of new cases that are diagnosed, which is close to one-hundred and fifty thousand every year and rising. That is why this event, and its success, is so important.</p>
					<div style="clear: both;"></div>
					<p style="float:right; width: 430px; margin-right:10px;">Every year this event is a huge success. We have many of our alumni come back to participate, along with other students from Georgia Tech and Georgia State, each person donating a set amount to enter into the tournament. This year however we are planning to aggressively expand the event to reach more people not only on Georgia Tech's and Georgia State's campuses, but also from other Atlanta colleges, such as Agnes Scott College and Emory University.</p>
					<img src="images/aboutImg2.jpg" alt="Crowds spectating another exciting Oozeball match." title="A crowed watching an exciting Oozeball match!" style="float: left; margin: 10px; box-shadow: 5px 5px 10px #000;" />
					<p style="float:right; width: 430px; margin-right:10px;">The annual Oozeball philanthropic event has created both a fun experience for all involved and an excellent source of donations to go towards the discovery of new treatments and preventive measurse for blood cancers.</p>

